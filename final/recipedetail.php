<h1 class="recipe-title">Ingredients</h1>
<h1 class="recipe-subtitle">For the chicken:</h1>
<ul class="ingredient-list">
<li>2 pounds chicken wings (about 16 drumettes and wingettes)</li>
<li>1.5 teaspoons salt (use 1 ts if using table salt)</li>
<li>pinch black pepper (or to taste)</li>
<li>1 teaspoon grated ginger</li>
<li>4-5 cups oil for frying about 2-inch deep (use deep, bottom heavy, medium size pot or pan)</li>
</ul>
<h1 class="recipe-subtitle">For the dry batter option:</h1>
<ul class="ingredient-list">
<li>1/2 cup potato starch (or cornstarch)</li>
</ul>
<h1 class="recipe-subtitle">For the wet batter option:</h1>
<ul class="ingredient-list">
<li>1/2 cup frying mix (aka tempura mix) or all-purpose flour with 1/2 teaspoon baking powder, 1/4 teaspoon salt, and optional 1/2 teaspoon garlic powder or onion powder. </li>
<li>1/4 cup potato starch (or cornstarch)</li>
<li>3/4 cup water</li>
</ul>
<h1 class="recipe-subtitle">For the soy garlic sauce:</h1>
<ul class="ingredient-list">
<li>2 tablespoons soy sauce </li>
<li>2 tablespoons rice wine (or mirin or other cooking wine)</li>
<li>2 tablespoons sugar</li>
<li>2 tablespoons honey (or 2.5 tablespoons corn syrup, rice syrup, or oligo syrup)</li>
<li>1 tablespoon minced garlic</li>
<li>1 teaspoon vinegar (white, rice, apple cider, etc.)</li>
<li>1 teaspoon or 2 teaspoons oyster sauce - optional</li>
<li>4 tablespoons water</li>
</ul>
<h1 class="recipe-subtitle">For the optional garnish:</h1>
<ul class="ingredient-list">
<li>1/2 teaspoon sesame seeds</li>
<li>1 tablespoon chopped scallion</li>
</ul>
<h1 class="recipe-title">Instructions</h1>
<ol class="recipe-number">
<li>Wash chicken wings, and drain thoroughly. Mix with the salt, pepper, and ginger. Let it sit in the fridge for 2 hours or longer (overnight to 24 hours).</li>
<li>Combine all the sauce ingredients of your choice and stir well. Boil over medium heat until it thickens slightly, about 4 to 5 minutes. Turn the heat off.</li>
<li>OPTION 1 Dry batter: If using the starch only, add the potato (or cornstarch) to the chicken, and rub it onto each wing well to coat evenly. Set aside.</li>
<li>OPTION 2 Wet batter: Mix the wet batter ingredients in a bowl, and stir well until smooth with no visible lumps.</li>
<li>Add the oil to a deep fryer, wok, or large pot. Heat the oil to 320°F (up to 330°F). Drop the chicken in the oil, one piece at a time. If using wet batter, dip each piece in the wet batter with tongs and shake off excess batter before dropping the chicken in the oil. Fry them in two batches. Cook until lightly golden, about 6 minutes, depending on the size of the chicken wings. Remove them with a wire skimmer or a slotted spoon. Drain on a wire rack or in a large mesh strainer set on a bowl.</li>
<li>Reheat the oil to 350°F (up to 360°F). Add the chicken (you can do this in one batch for the second frying), and deep fry again, for about 5 minutes, until golden brown. Drain on a wire rack or in a large mesh strainer set on a bowl.</li>
<li>You can either toss the fried chicken pieces in the sauce or hand-brush them. Sprinkle with the optional sesame seeds or chopped scallion to serve.</li>
</ol>
